    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (id_kelas.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "id kelas tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            id_kelas.requestFocus();
        } else if (id_jurusan.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "id jurusan tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            id_jurusan.requestFocus();
        } else if (nama_kelas.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "nama tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nama_kelas.requestFocus();
        } else if (nama_ruangan.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "nama ruangan tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nama_ruangan.requestFocus();
        } else {
            if (modeSimpan == true) { // save
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("insert into kelas values(?,?,?,?)");
                    prpr.setString(1, id_kelas.getText());
                    prpr.setString(2, id_jurusan.getText());
                    prpr.setString(3, nama_kelas.getText());
                    prpr.setString(4, nama_ruangan.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else { // update
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("update kelas set nama_ruangan = ? where id_kelas = ?");
                    prpr.setString(1, nama_ruangan.getText());
                    prpr.setString(2, id_kelas.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            batalActionPerformed(evt);
            Read();
        }
    }//GEN-LAST:event_simpanActionPerformed

