    private void nisnKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nisnKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            dupe = Search(nisn.getText());
            if (dupe == true) {
                dupe = false;
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(
                        this,
                        "data kosong atau kode jurusan " + nisn.getText() + " sudah ada!"
                        + "Apakah anda ingin mengubah/menghapus data?",
                        "Konfirmasi",
                        JOptionPane.YES_NO_OPTION);
                if (pilihan == 0) {
                    try {
                        Statement stm = setConnection().createStatement();
//                        ResultSet rs = stm.executeQuery("select id_kelas,nama_siswa,alamat,no_hp,id_angkatan from siswa where nisn =" + nisn.getText());
                        ResultSet rs = stm.executeQuery("select * from siswa where nisn =" + nisn.getText());
                        ResultSetMetaData rsmt = rs.getMetaData();
                        while (rs.next()) {
                            kelas.setText(rs.getString(rsmt.getColumnName(2)));
                            nama_siswa.setText(rs.getString(rsmt.getColumnName(3)));
                            alamat.setText(rs.getString(rsmt.getColumnName(4)));
                            nohp.setText(rs.getString(rsmt.getColumnName(5)));
                            angkatan.setText(rs.getString(rsmt.getColumnName(6)));
                        }
                        nisn.setEnabled(true);
                        nisn.setEditable(false);
                        kelas.setEnabled(true);
                        kelas.setEditable(false);
                        pilihKelas.setEnabled(true);
                        nama_siswa.setEnabled(true);
                        nama_siswa.setEditable(false);
                        alamat.setEnabled(true);
                        nohp.setEnabled(true);
                        angkatan.setEnabled(true);
                        angkatan.setEditable(false);
                        pilihAngkatan.setEnabled(false);
                        baru.setEnabled(false);
                        simpan.setEnabled(true);
                        ubah.setEnabled(false);
                        batal.setEnabled(true);
                        hapus.setEnabled(false);
                        modeSimpan = false;
                    } catch (SQLException e) {
                        System.err.println("Query error");
                    }
                } else {
                    nisn.setText("");
                    nisn.requestFocus();
                }
            } else {
                kelas.setEnabled(true);
                kelas.setEditable(false);
                pilihKelas.setEnabled(true);
                pilihKelas.requestFocus();
                nama_siswa.setEnabled(true);
                alamat.setEnabled(true);
                nohp.setEnabled(true);
                angkatan.setEnabled(true);
                angkatan.setEditable(false);
                pilihAngkatan.setEnabled(true);
                simpan.setEnabled(true);
            }
        }
    }//GEN-LAST:event_nisnKeyReleased

