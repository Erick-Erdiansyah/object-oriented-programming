    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (nisn.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "nisn tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nisn.requestFocus();
        } else if (kelas.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "kelas tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            pilihKelas.requestFocus();
        } else if (nama_siswa.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "nama tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nama_siswa.requestFocus();
        } else if (alamat.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "alamat tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            alamat.requestFocus();
        } else if (nohp.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "nomor hp tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nohp.requestFocus();
        } else if (angkatan.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "angkatan tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            pilihAngkatan.requestFocus();
        } else {
            if (modeSimpan == true) { // save
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("insert into siswa values(?,?,?,?,?,?)");
                    prpr.setString(1, nisn.getText());
                    prpr.setString(2, kelas.getText());
                    prpr.setString(3, nama_siswa.getText());
                    prpr.setString(4, alamat.getText());
                    prpr.setString(5, nohp.getText());
                    prpr.setString(6, angkatan.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else { // update
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("update siswa set kelas = ?, alamat = ?, nohp = ? where nisn = ?");
                    prpr.setString(1, kelas.getText());
                    prpr.setString(2, alamat.getText());
                    prpr.setString(3, nohp.getText());
                    prpr.setString(4, nisn.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            batalActionPerformed(evt);
            Read();
        }
    }//GEN-LAST:event_simpanActionPerformed

