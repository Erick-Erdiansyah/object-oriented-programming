    private void id_transaksiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_id_transaksiKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            dupe = Search(id_transaksi.getText());
            if (dupe == true) {
                dupe = false;
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(
                        this,
                        "data kosong atau id transaksi " + id_transaksi.getText() + " sudah ada!"
                        + "Apakah anda ingin mengubah/menghapus data?",
                        "Konfirmasi",
                        JOptionPane.YES_NO_OPTION);
                if (pilihan == 0) {
                    try {
                        Statement stm = setConnection().createStatement();
//                        ResultSet rs = stm.executeQuery("select id_kelas,nama_siswa,alamat,no_hp,id_angkatan from siswa where nisn =" + nisn.getText());
                        ResultSet rs = stm.executeQuery("select * from transaksi where id_transaksi =" + id_transaksi.getText());
                        ResultSetMetaData rsmt = rs.getMetaData();
                        while (rs.next()) {
                            id_admin.setText(rs.getString(rsmt.getColumnName(1)));
                            tanggal.setText(rs.getString(rsmt.getColumnName(2)));
                            nisn.setText(rs.getString(rsmt.getColumnName(4)));
                            id_kelas.setText(rs.getString(rsmt.getColumnName(5)));
                            semester.setText(rs.getString(rsmt.getColumnName(6)));
                        }
                        id_transaksi.setEnabled(false);
                        id_admin.setEnabled(false);
                        tanggal.setEnabled(true);
                        nisn.setEnabled(false);
                        id_kelas.setEnabled(false);
                        semester.setEnabled(false);
                        baru.setEnabled(false);
                        simpan.setEnabled(true);
                        ubah.setEnabled(false);
                        batal.setEnabled(true);
                        hapus.setEnabled(false);
                        modeSimpan = false;
                    } catch (SQLException e) {
                        System.err.println("Query error");
                    }
                } else {
                    id_transaksi.setText("");
                    id_transaksi.requestFocus();
                }
            } else {
                id_admin.setEnabled(true);
                id_admin.requestFocus();
                tanggal.setEnabled(true);
                nisn.setEnabled(true);
                id_kelas.setEnabled(true);
                semester.setEnabled(true);
                simpan.setEnabled(true);
            }
        }
    }//GEN-LAST:event_id_transaksiKeyReleased

