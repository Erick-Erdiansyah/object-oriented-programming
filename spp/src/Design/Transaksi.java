    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (id_transaksi.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "id tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            id_transaksi.requestFocus();
        } else if (id_admin.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "id tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            id_admin.requestFocus();
        } else if (tanggal.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "tanggal tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            tanggal.requestFocus();
        } else if (nisn.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "nisn tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nisn.requestFocus();
        } else if (id_kelas.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "kelas tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            id_kelas.requestFocus();
        } else if (semester.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "semester tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            semester.requestFocus();
        } else {
            if (modeSimpan == true) { // save
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("insert into transaksi values(?,?,?,?,?,?)");
                    prpr.setString(1, id_transaksi.getText());
                    prpr.setString(2, id_admin.getText());
                    prpr.setString(3, tanggal.toString());
                    prpr.setString(4, nisn.getText());
                    prpr.setString(5, id_kelas.getText());
                    prpr.setString(6, semester.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else { // update
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("update transaksi set tanggal = ? where id_transaksi = ?");
                    prpr.setString(1, tanggal.toString());
                    prpr.setString(2, id_transaksi.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(siswa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            batalActionPerformed(evt);
            Read();
        }
    }//GEN-LAST:event_simpanActionPerformed

