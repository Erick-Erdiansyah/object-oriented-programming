    private boolean regDriver() {
        try {
            DriverManager.registerDriver(new Driver());
            return true;
        } catch (SQLException e) {
            System.out.println(e);
            return false;
        }
    }

    private Connection setConnection() throws SQLException {
        regDriver();
        String url = "jdbc:mysql://localhost:3306/spp_1.1";
        String user = "root";
        String passwd = "";
        return DriverManager.getConnection(url, user, passwd);
    }

    private void Read() {
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        try {
            Statement stm = setConnection().createStatement();
            ResultSet rs = stm.executeQuery("select id_angkatan,angkatan,semester from angkatan");
            ResultSetMetaData rsmt = rs.getMetaData();
            model.setRowCount(0);
            model.fireTableDataChanged();
            while (rs.next()) {
                id_angkatan = rs.getString(rsmt.getColumnName(1));
                model.addRow(new Object[]{rs.getString(rsmt.getColumnName(2)), rs.getString(rsmt.getColumnName(3))});
            }
        } catch (SQLException ex) {
            Logger.getLogger(PilihAngkatan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        if (tabel.getSelectedRow() == -1) {
            jButton1.setEnabled(false);
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih salah satu baris yang diinginkan!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            kode.setText(tabel.getValueAt(tabel.getSelectedRow(), 0).toString());
            nama.setText(tabel.getValueAt(tabel.getSelectedRow(), 1).toString());
            jButton1.setEnabled(true);
            nama.setEditable(false);
            kode.setEditable(false);
        }
    }//GEN-LAST:event_tabelMouseClicked

