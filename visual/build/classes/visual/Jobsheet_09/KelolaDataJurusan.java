    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        if (tabel.getSelectedRow() == -1) {
            // Jika tidak ada baris dari jTable1 yang terpilih (terseleksi)
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih salah satu baris yang akan dihapus!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            // Jika ada baris dari jTable1 yang terpilih
            int pilihan = JOptionPane.showConfirmDialog(
                    this,
                    "Apakah yakin akan menghapus data?",
                    "Konfirmasi",
                    JOptionPane.YES_NO_OPTION
            );
            if (pilihan == 0) { // tombol yes dipilih
                // menghapus baris dari jTable1 yang terpilih
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("delete from jurusan where kode_jurusan = ?");
                    prpr.setString(1, kode_jurusan.getText());
                    var i = prpr.executeUpdate();
                    Read();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Data Jurusan Digunakan di Tabel lain");
                }
                cancelActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

