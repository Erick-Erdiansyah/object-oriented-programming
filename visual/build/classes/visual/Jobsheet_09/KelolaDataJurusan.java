    private void kode_jurusanKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kode_jurusanKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            dupe = Search(kode_jurusan.getText());
            if (dupe == true) {
                dupe = false;
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(
                        this,
                        "data kosong atau kode jurusan " + kode_jurusan.getText() + " sudah ada!"
                        + "Apakah anda ingin mengubah/menghapus data?",
                        "Konfirmasi",
                        JOptionPane.YES_NO_OPTION);
//                System.out.println(pilihan);
                if (pilihan == 0) {
                    try {
                        Statement stm = setConnection().createStatement();
                        ResultSet rs = stm.executeQuery("select nama_jurusan from jurusan where kode_jurusan =" + kode_jurusan.getText());
                        ResultSetMetaData rsmt = rs.getMetaData();
                        while (rs.next()) {
                            name_jurusan.setText(rs.getString(rsmt.getColumnName(1)));
                        }
                        name_jurusan.setEnabled(false);
                        kode_jurusan.setEnabled(false);
                        baru.setEnabled(false);
                        save.setEnabled(false);
                        edit.setEnabled(true);
                        delete.setEnabled(true);
                        cancel.setEnabled(true);
                        modeSimpan = false;
                    } catch (SQLException e) {
                        System.err.println("Query error");
                    }
                } else {
                    kode_jurusan.setText("");
                    kode_jurusan.requestFocus();
                }
            } else {
                name_jurusan.setEnabled(true);
                name_jurusan.requestFocus();
                save.setEnabled(true);
            }
        }
    }//GEN-LAST:event_kode_jurusanKeyReleased

