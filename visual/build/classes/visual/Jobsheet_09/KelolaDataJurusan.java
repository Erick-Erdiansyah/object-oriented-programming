    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        if (kode_jurusan.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "kode jurusan tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            kode_jurusan.requestFocus();
        } else if (name_jurusan.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Nama tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            name_jurusan.requestFocus();
        } else {
            if (modeSimpan == true) { // save
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("insert into jurusan values(?,?)");
                    prpr.setString(1, kode_jurusan.getText());
                    prpr.setString(2, name_jurusan.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(latihan.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else { // update
                try {
                    PreparedStatement prpr = setConnection().prepareStatement("update jurusan set nama_jurusan = ? where kode_jurusan = ?");
                    prpr.setString(1, name_jurusan.getText());
                    prpr.setString(2, kode_jurusan.getText());
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(latihan.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            cancelActionPerformed(evt);
            Read();
        }
    }//GEN-LAST:event_saveActionPerformed

