    private void konversiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_konversiActionPerformed
        // main thing done here (￣▽￣)"
        try {
            Float hasil = 0f;
            Float celcius = Float.valueOf(Celcius.getText());
            switch (combo.getSelectedIndex()) {
                case 0:
                    // celcius to fahrenheit
                    hasil = (celcius * 9f/5f) + 32f; 
                    break;
                case 1:
                    // celcius to reamur
                    hasil = (4f/5f)*celcius;
                    break;
                case 2:
                    // celcius to kelvin
                    hasil = celcius + 273f;
                    break;
                default:
                    // do something o(*￣▽￣*)ブ
                    break;
            }
            Hasil.setText(Float.toString(hasil));
        } catch (NumberFormatException e) {
            Hasil.setText("Angry !!! ╰（‵□′）╯");
        }      
    }//GEN-LAST:event_konversiActionPerformed

