    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        if (nim.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "NIM tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nim.requestFocus();
        } else if (nama.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Nama tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            nama.requestFocus();
        } else if (gender.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "gender tidak boleh kosong!", // isi pesan
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE
            );
            gender.requestFocus();
        } else {
            if (modeSimpan == true) { // save
                try {
                    String kode_prodi = String.valueOf(Prodi_1.GetCode());
                    PreparedStatement prpr = setConnection().prepareStatement("insert into mhs values(?,?,?,?)");
                    prpr.setString(1, nim.getText());
                    prpr.setString(2, nama.getText());
                    prpr.setString(3, gender.getItemAt(gender.getSelectedIndex()));
                    prpr.setString(4, kode_prodi);
                    var i = prpr.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(latihan.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else { // update
                try {
                    String kode_prodi = null;
                    try {
                        kode_prodi = String.valueOf(Prodi_1.GetCode());
                    } catch (NumberFormatException e) {
                        kode_prodi = "";
                    }
                    if (!kode_prodi.equals("")) {
                        PreparedStatement prpr = setConnection().prepareStatement("update mhs set nama_mhs = ?, gender = ?, kode_prodi = ? where nim = ?");
                        prpr.setString(1, nama.getText());
                        prpr.setString(2, gender.getItemAt(gender.getSelectedIndex()));
                        prpr.setString(3, kode_prodi);
                        prpr.setString(4, nim.getText());
                        var i = prpr.executeUpdate();
                    } else {
                        PreparedStatement prpr = setConnection().prepareStatement("update mhs set nama_mhs = ?, gender = ? where nim = ?");
                        prpr.setString(1, nama.getText());
                        prpr.setString(2, gender.getItemAt(gender.getSelectedIndex()));
                        prpr.setString(3, nim.getText());
                        var i = prpr.executeUpdate();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(latihan.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            cancelActionPerformed(evt);
            Read();
        }
    }//GEN-LAST:event_saveActionPerformed

