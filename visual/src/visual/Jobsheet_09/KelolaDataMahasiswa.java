    private void nimKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nimKeyReleased
        boolean dupe;
        String parameter = "nim";
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            dupe = Search(parameter, nim.getText());
            if (dupe == true) {
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(
                        this,
                        "Kode prodi " + nim.getText() + " sudah ada! "
                        + "Apakah anda ingin mengubah/menghapus data?",
                        "Konfirmasi",
                        JOptionPane.YES_NO_OPTION
                );
                if (pilihan == 0) {
                    try {
                        Statement stm = setConnection().createStatement();
                        ResultSet rs = stm.executeQuery("select * from mhs where nim =" + nim.getText());
                        ResultSetMetaData rsmt = rs.getMetaData();
                        while (rs.next()) {
                            nim.setText(rs.getString(rsmt.getColumnName(1)));
                            nama.setText(rs.getString(rsmt.getColumnName(2)));
                            if (rs.getString(rsmt.getColumnName(3)).equals("L")) {
                                gender.setSelectedIndex(0);
                            } else {
                                gender.setSelectedIndex(1);
                            }
                            prodi.setText(rs.getString(rsmt.getColumnName(4)));
                        }
                        gender.setEnabled(false);
                        nama.setEnabled(false);
                        nim.setEnabled(false);
                        baru.setEnabled(false);
                        save.setEnabled(false);
                        edit.setEnabled(true);
                        delete.setEnabled(true);
                        cancel.setEnabled(true);
                        modeSimpan = false;
                    } catch (SQLException e) {
                        System.err.println("Query error");
                    }
                } else {
                    nim.setText("");
                    nim.requestFocus();
                }
            } else {
                nama.setEnabled(true);
                gender.setEnabled(true);
                pilih.setEnabled(true);
                prodi.setEnabled(true);
                nama.requestFocus();
                save.setEnabled(true);
            }
        }
    }//GEN-LAST:event_nimKeyReleased

